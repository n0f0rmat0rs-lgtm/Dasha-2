# Нормализация: верхний регистр, убираем лишние пробелы

def normalize(text: str) -> str:
    return text.strip().upper()

async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "Привет.\n\n"
        "Это бот-квест. Вводи кодовые слова — получай записки.\n\n"
        "Первое слово у тебя уже есть. Удачи 🖤"
    )

async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    user_input = normalize(update.message.text)
    response = NOTES.get(user_input)

    if response:
        await update.message.reply_text(response)
    else:
        await update.message.reply_text(
            "Не то слово. Поищи ещё раз — оно где-то рядом 🔍"
        )

def main():
    app = Application.builder().token(TOKEN).build()
    app.add_handler(CommandHandler("start", start))
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))
    logger.info("Бот запущен")
    app.run_polling()

if __name__ == "__main__":
    main()
